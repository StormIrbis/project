function findMaxNumber(a, b, c, d) {
  if (
    typeof a !== "number" ||
    typeof b !== "number" ||
    typeof c !== "number" ||
    typeof d !== "number" ||
    a === null ||
    b === null ||
    c === null ||
    d === null
  ) {
    return 0;
  } else {
    return Math.max(a, b, c, d);
  }
}

